/**
 * Get rune info by ID
 * @param {number|string} id - Rune ID (e.g. 5245)
 * @returns {Object|null} Rune data object
 */
export function getRuneById(id) {
  return RUNE_DATA[id] || null;
}

/**
 * Get runes by type and tier
 * @param {string} type - "mark", "seal", "glyph", "quintessence"
 * @param {number} tier - 1, 2, or 3
 * @returns {Array} Array of rune data objects
 */
export function getRunesByType(type, tier = 3) {
  return Object.values(RUNE_DATA).filter(r => r.type === type && r.tier === tier);
}

/**
 * Get rune icon URL from CDN
 * @param {string} icon - Icon name (e.g. "r_1_3")
 * @param {string} cdnHost - CDN host URL
 * @returns {string} Full icon URL
 */
export function getRuneIconUrl(icon, cdnHost) {
  if (!icon) return "";
  return `${cdnHost}/rune/${icon}.png`;
}

/**
 * Get rune type for a slot position
 * @param {number} slotPosition - Slot 1-30
 * @returns {string} Rune type name
 */
export function getSlotType(slotPosition) {
  if (slotPosition >= 1 && slotPosition <= 9) return "mark";
  if (slotPosition >= 10 && slotPosition <= 18) return "seal";
  if (slotPosition >= 19 && slotPosition <= 27) return "glyph";
  if (slotPosition >= 28 && slotPosition <= 30) return "quintessence";
  return "unknown";
}

/**
 * Get valid Greater rune IDs for a slot position
 * @param {number} slotPosition - Slot 1-30
 * @returns {number[]} Array of valid rune IDs
 */
export function getValidRuneIdsForSlot(slotPosition) {
  const type = getSlotType(slotPosition);
  return Object.keys(RUNE_DATA)
    .filter(id => RUNE_DATA[id].type === type && RUNE_DATA[id].tier === 3)
    .map(Number);
}

